#Этот бот умеет
#1. Принимать текстовые сообщения пользователя и отправлять их к YGPT и возвращать ответ
#2. Принимать голосвые сообщения пользователя и отправлять их к YGPT и возвращать ответ
#3. По команде /stt переводить голосовые сообщения в текст без обращения к YGPT
#4. По команде /tts переводить текстовые сообщения в голосовые без обращения к YGPT
#5. Подсчитывать расход токеннов символов stt и tts блоков
#6. По команде /debug отправлять файл с логами
#
# Файлы:
# main - тело для взаимодействия с телеграммом
# gpt - файл для взаимодействия с YGPT
# spichkit - файл для взаимодействия со spechkit (перевод текста в аудио и аудио в текст)
# database - файл для заполнения базы данных sqlite
# speech_kit - база данных sqlite
